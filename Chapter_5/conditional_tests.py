# Conditional tests using in and not in
car = ["subaru", "audi", "toyota", "honda"]
print("\nIs 'subaru' in car? I predict True.")
print("subaru" in car)
print("\nIs 'ford' in car? I predict False.")
print("ford" in car)
print("\nIs 'ford' not in car? I predict True.")
print("ford" not in car)
print("\nIs 'subaru' not in car? I predict False.")
print("subaru" not in car)